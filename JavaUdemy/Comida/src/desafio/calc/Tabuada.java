package desafio.calc;

import java.util.Scanner;

public class Tabuada {
    public static void main(String[] args) {

        int i = 0;
        int j = 0;
        



        for(i = 1; i <= 10; i++){
            System.out.println("desafio.calc.Tabuada do " + i);
            for (j = 1; j <= 10; j++){
                System.out.println(i + " x " + j + " = " + i*j);


            }

        }




    }


}
